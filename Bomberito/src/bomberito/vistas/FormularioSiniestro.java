    private void CoordXKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CoordXKeyTyped
        if (CoordX.getText().length() >= 3) {
            evt.consume();
        }
        int key = evt.getKeyChar();
        boolean num = key >= 48 && key <= 57;
        if (!num) {
            evt.consume();
        }
    }//GEN-LAST:event_CoordXKeyTyped

